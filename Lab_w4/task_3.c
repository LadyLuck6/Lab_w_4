#include <stdio.h>
#include <math.h>

int main()
{
    int j, i, s, p = 0; // ініціалізація змінних j, i, s та p, де змінна p починається з 0
    // цикл для значень і від 1 до 10
    for (i = 1; i <= 10; i++)
    {   
        for (s = 1, j = 1; j <= i; j++)
            s *= (i + j); 
        p += s; // додавання значення s до змінної p
    }
    printf("p=%d\n", p); // виведення значення p на екран

    return 0; // повернення 0, означає успішне виконання програми
};
